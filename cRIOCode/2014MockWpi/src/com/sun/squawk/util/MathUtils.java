package com.sun.squawk.util;

public class MathUtils {

    public static double log(double x)
    {
        return Math.log(x);
    }

    public static double pow(double a, int b)
    {
        return Math.pow(a, b);
    }

    public static double atan2(double y, double x)
    {
        return Math.atan2(y, x);
    }

}
